  <h2>VIII. STUDENT CONDUCT AND DISCIPLINE</h2>
  <p>
    This section refers to the implementation of university rules and regulations governing student behavior and conduct.
  </p>

  <h3>A. STATEMENT OF POLICIES</h3>
  <ul>
    <li>CTU strives to develop individuals to become responsible and productive members of society. Its responsibility is not only confined to teaching and developing technical skills but also in inculcating values and discipline in its students.</li>
    <li>The students’ enjoyment of their rights shall be balanced by the exercise of accountability and social responsibility, that is, for every right enjoyed, there is a corresponding duty and accountability.</li>
    <li>The rules and regulations define appropriate student conduct and determine sanctions for offenses/misconducts that endanger life, threaten peace and order inside the campus, or damage university/personal property.</li>
    <li>Due process shall be employed in dealing with student offenses/misconducts.</li>
  </ul>

  <h3>B. SCOPE</h3>
  <p>These rules and regulations apply to students officially enrolled in the university.</p>

